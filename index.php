        <section id="historia">
            <h2>Historia de la Carrera de Negocios Digitales</h2>
            <p>La carrera de Negocios Digitales nació en el año 2020 en respuesta a la creciente necesidad de transformación digital de las empresas en Ecuador, especialmente impulsada por los efectos de la pandemia mundial de COVID-19. Durante este periodo, muchas empresas tradicionales se vieron obligadas a adaptarse rápidamente a un entorno online para sobrevivir. El aumento de la demanda por soluciones digitales y la necesidad de gestionar negocios a través de plataformas digitales se convirtió en una oportunidad única para formar profesionales capaces de liderar este cambio.</p>
            
            <p>El contexto global, con restricciones sanitarias y cuarentenas, dejó en evidencia la importancia de contar con una infraestructura digital sólida. Las empresas que ya habían invertido en su presencia online tuvieron una ventaja significativa, mientras que aquellas que no se habían digitalizado sufrieron grandes pérdidas. Fue en este escenario donde surgió la necesidad de crear una carrera que preparara a los estudiantes con los conocimientos y herramientas necesarias para gestionar negocios digitales, tanto en el ámbito de la administración como en el desarrollo de soluciones tecnológicas.</p>
            
            <p>Con la rápida evolución de las tecnologías y la globalización de los mercados, los graduados de la carrera de Negocios Digitales están capacitados para afrontar los retos de un mercado laboral altamente competitivo, promoviendo la innovación y la sostenibilidad en los negocios online. A través de una malla académica que combina conocimientos de marketing digital, programación, finanzas y gestión empresarial, los egresados se convierten en agentes de cambio en el mundo digital, ayudando a las empresas a evolucionar y a aprovechar las nuevas oportunidades de negocio en la era digital.</p>
